"""Shared statistical helpers."""

from __future__ import annotations

_GRADES = ("A", "B", "C", "D", "F")


def _median(values: list[float]) -> float | None:
    if not values:
        return None
    ordered = sorted(values)
    mid = len(ordered) // 2
    if len(ordered) % 2:
        return ordered[mid]
    return (ordered[mid - 1] + ordered[mid]) / 2
